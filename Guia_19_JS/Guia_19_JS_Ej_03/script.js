// Escriba un programa en donde se 
// pida la edad del usuario. Si el usuario es mayor de
// edad se debe mostrar un mensaje indicándolo.

var edad = prompt("Ingrese la edad del usuario");

if (edad >= 18) {
    alert("El usuario es mayor de edad, tiene " + edad + " años.");
} else {
    alert("El usuario es mejor de edad, tiene " + edad + " años.");
}
